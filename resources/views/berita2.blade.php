                20 Oktober 2021, Aplikasi Rapor Elektronik (eRapor) Sekolah Menengah Atas (SMA) mulai dikembangkan oleh Direktorat SMA sejak tahun 2014. 
                Pada awal pengembangannya, aplikasi tersebut mengacu pada struktur Kurikulum 2013 dan Permendikbud Nomor 23 Tahun 2016 tentang Standar Penilaian Pendidikan. 
                Sejak Kurikulum 2013 diterapkan pada pelaporan hasil belajar siswa, isu besar dan permasalahan yang paling sering dikeluhkan oleh guru adalah rumitnya proses penilaian dan pengadministrasiannya. 
