                Sistem Kredit Semester (SKS), mata pelajaran informatika, dan Kurikulum pada Kondisi Darurat merupakan contoh berbagai kebijakan yang telah disesuaikan ke dalam aplikasi eRapor SMA.
                Namun,  saat ini berbagai permasalahan lain juga muncul dan dirasakan oleh para pengguna, diantaranya aplikasi semakin berat untuk diakses terutama bagi sekolah-sekolah yang sejak awal pengembangan telah menggunakannya, dan diperlukannya fitur-fitur tambahan yang mempermudah pengguna dalam penyusunan laporan hasil belajar. Berdasarkan permasalahan tersebut, Direktorat SMA perlu melakukan evaluasi dan merencanakan tindak lanjut pengembangan eRapor SMA melalui kegiatan workshop Pembaruan Aplikasi eRapor SMA Tahun 2021. 
